<script>
export default {
    data: () => ({
        userList: [],
    }),
    methods: {
        async fetchUsers() {
            this.userList = await fetch('https://jsonplaceholder.typicode.com/users').then(response => response.json())
        },
    },
    
    created() {
        this.fetchUsers();
    },
};
</script>

<template>
  <main>
    <h1>Users</h1>
    <ul>
        <li v-for="user in userList" :key="`user-${user.id}`">
            {{ user.name }}: {{ user.website }}
        </li>
    </ul>
    <!-- <pre>{{ userList }}</pre> -->
  </main>
</template>

